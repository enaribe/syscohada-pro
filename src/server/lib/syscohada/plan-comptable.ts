export function rechercheCompte(query: string): { compte: string; intitule: string }[] {
  const q = query.toLowerCase()
  return Object.entries(PLAN_COMPTABLE)
    .filter(([compte, intitule]) => compte.includes(q) || intitule.toLowerCase().includes(q))
    .map(([compte, intitule]) => ({ compte, intitule }))
    .slice(0, 20)
}

export function validerCompte(compte: string): boolean {
  return compte in PLAN_COMPTABLE || /^\d{6}$/.test(compte)
}
